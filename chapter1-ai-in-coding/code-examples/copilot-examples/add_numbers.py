def add_numbers(a, b):
    """
    Add two numbers and return the result.
    
    Args:
        a: First number
        b: Second number
    
    Returns:
        The sum of a and b
    """
    return a + b

# Example usage
if __name__ == "__main__":
    num1 = 3
    num2 = 4
    result = add_numbers(num1, num2)
    print(f"Result: {result}")